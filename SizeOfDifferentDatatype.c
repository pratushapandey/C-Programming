// Displayimg Size of Different Datatype,Format Specifier -%lu

#include<stdio.h>

int main()
{
	printf("Size of int : %lu",sizeof(int));
	printf("\nSize of float : %lu",sizeof(float));
	printf("\nSize of char : %lu",sizeof(char));
	printf("\nSize of double : %lu",sizeof(double));
	printf("\nSize of short int : %lu",sizeof(short int));
	printf("\nSize of long int : %lu",sizeof(long int));
	printf("\nSize of signed int : %lu",sizeof(signed int));
	printf("\nSize of unsigned int : %lu",sizeof(unsigned int));
}
